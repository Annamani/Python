# Lists can be declared within  [] 
fruits=["apple","grapes","watermelon","mango"]
print(fruits)
print(fruits[2])
print(fruits[1:])
print(fruits[:2])
print(fruits[:-3])
numbers=["1","2","3","4","5"]
print(numbers)
new_list=[fruits,numbers]
print(new_list)
numbers.append(9)
print(numbers)

